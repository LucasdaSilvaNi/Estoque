        public List<TB_TRANSACAO> ListarTransacao(int moduloId)
        {
            //Exemplo para sort
            Expression<Func<TB_TRANSACAO, string>> sort = a => a.TB_TRANSACAO_DESCRICAO;

            //Exemplo para Where
            Expression<Func<TB_TRANSACAO, bool>> where = a => a.TB_MODULO_ID == moduloId;

            TransacaoInfrastructure persistencia = new TransacaoInfrastructure();
            var result = persistencia.SelectWhere(sort, false, where, this.SkipRegistros);

            this.TotalRegistros = persistencia.TotalRegistros;

            return result;
        }	

